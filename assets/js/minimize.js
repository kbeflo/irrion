function minimize21() {
  var x = document.getElementById("minimize-col-1-1");
  var y = document.getElementById("minimize-col-1-2");
  var a = document.getElementsByClassName("line-col-2-1")[0];
  var b = document.getElementsByClassName("line-col-2-1")[1];
  if (x.style.opacity === "0") {
    x.style.opacity = "1";
    y.style.opacity = "1";
    a.style.opacity = "1";
    b.style.opacity = "1";
  } else {
    x.style.opacity = "0";
    y.style.opacity = "0";
    a.style.opacity = "0";
    b.style.opacity = "0";
  }
}

